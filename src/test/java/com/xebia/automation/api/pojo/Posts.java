package com.xebia.automation.api.pojo;

public class Posts {
private int userid;
private int id;
private String title;
private String body;

public int getUserId() {
	return userid;
}
public void setUserId(int userId) {
	this.userid = userId;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public String getBody() {
	return body;
}
public void setBody(String body) {
	this.body = body;
}

}
